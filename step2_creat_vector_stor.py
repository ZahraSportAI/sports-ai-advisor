from langchain_huggingface import HuggingFaceEmbeddings
from langchain_chroma import Chroma
from langchain_core.documents import Document

# ==================================================
# داده‌های غنی‌شده ورزشی (برای تست حرفه‌ای)
# ==================================================
texts = [
    # تمرینات سرعت و تکواندو
    "تمرینات پلایومتریک مانند جامپینگ جک، پرش عمودی و پرش طول، برای افزایش سرعت و توان انفجاری در تکواندو بسیار موثر هستند.",
    "برای بهبود سرعت پا در تکواندو، تمرینات نردبان چابکی (Agility Ladder) و دویدن سرعتی (Sprint) توصیه می‌شود.",
    "در تکواندو، ترکیب تمرینات پلایومتریک با تمرینات تکنیکی (ضربات پیاپی) سرعت اجرای ضربه را تا ۳۰ درصد افزایش می‌دهد.",

    # تمرینات استقامتی
    "تمرینات استقامتی باعث افزایش تعداد میتوکندری در سلول‌های عضلانی و بهبود عملکرد قلبی-عروقی می‌شود.",
    "دویدن با شدت متوسط به مدت ۳۰ دقیقه، ۳ جلسه در هفته، استقامت عمومی ورزشکاران را افزایش می‌دهد.",

    # بدنسازی و پروتئین
    "در بدنسازی، مصرف ۲۰ تا ۳۰ گرم پروتئین با کیفیت (مانند وی یا کازیین) بلافاصله بعد از تمرین، سنتز پروتئین عضلانی را به حداکثر می‌رساند.",
    "پروتئین مصرفی بعد از تمرین باید در عرض ۳۰ دقیقه طلایی پس از تمرین مصرف شود تا بهترین جذب را داشته باشد.",

    # تمرین برای مبتدیان
    "سه جلسه تمرین قدرتی در هفته برای افراد مبتدی کافی است تا رشد عضلانی مطلوبی داشته باشند.",
    "برنامه تمرینی مبتدیان باید شامل ۸ تا ۱۲ تکرار در هر ست و ۲ تا ۳ ست برای هر حرکت باشد.",
]

# تبدیل متن‌ها به Document
documents = [Document(page_content=text) for text in texts]

# مدل embedding
print("🔄 در حال بارگذاری مدل embedding...")
embeddings = HuggingFaceEmbeddings(
    model_name="sentence-transformers/all-MiniLM-L6-v2",
    model_kwargs={'device': 'cpu'},
    encode_kwargs={'normalize_embeddings': True}
)

# حذف پایگاه داده قبلی و ساختن جدید
print("🔄 در حال ساخت پایگاه داده جدید...")
vector_store = Chroma.from_documents(
    documents=documents,
    embedding=embeddings,
    persist_directory="./chroma_db"  # همین پوشه قبلی را بازنویسی می‌کند
)

print("✅ پایگاه داده جدید با موفقیت ساخته شد.")
print(f"📊 تعداد اسناد ذخیره شده: {vector_store._collection.count()}")